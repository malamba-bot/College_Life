/**
 * Render a string as vector line segments.
 * @param {string} text - The text to render
 * @param {number} x - Top-left X position
 * @param {number} y - Top-left Y position
 * @param {number} scale - Character scale (each char is 4*scale wide, 6*scale tall)
 * @param {number} [spacing=1] - Extra gap between chars (in scale units)
 * @returns {Array<{x1: number, y1: number, x2: number, y2: number}>}
 */
export function vectorText(text, x, y, scale, spacing = 1) {
  const lines = [];
  const charWidth = 4 * scale + spacing * scale;
  let cx = x;

  for (const ch of text.toUpperCase()) {
    const strokes = CHARS[ch];
    if (strokes) {
      for (const s of strokes) {
        lines.push({
          x1: cx + s[0] * scale,
          y1: y + s[1] * scale,
          x2: cx + s[2] * scale,
          y2: y + s[3] * scale,
        });
      }
    }
    cx += charWidth;
  }

  return lines;
}

/**
 * Get the pixel width of a string at given scale.
 * @param {string} text
 * @param {number} scale
 * @param {number} [spacing=1]
 * @returns {number}
 */
export function vectorTextWidth(text, scale, spacing = 1) {
  const charWidth = 4 * scale + spacing * scale;
  return text.length * charWidth - spacing * scale;
}
